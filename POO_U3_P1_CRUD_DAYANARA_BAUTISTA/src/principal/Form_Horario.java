     public void mostrarDatos(){
        String titulos[]={"Nombre Laboratorio","N° Laboratorio","Asignatura","Día","Hora"};
        String registro[]=new String [6];
        DefaultTableModel modelo=new DefaultTableModel(null, titulos);
        String SQL = "SELECT * FROM `horarios`";
        try{
            Statement st=(Statement) con.createStatement();
            ResultSet rs=st.executeQuery(SQL);
            while(rs.next()){
            registro [0]=rs.getString("hor_nomLab");
            registro [1]=rs.getString("hor_numLab");
            registro [2]=rs.getString("hor_asignatura");
            registro [3]=rs.getString("hor_dia");
            registro [4]=rs.getString("hor_hora");
            modelo.addRow(registro);
        }
            TablaHorarios.setModel(modelo);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error al mostrar los datos: "+e);
        }
    }
